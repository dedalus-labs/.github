function githubToken() {
  if (process.env.GITHUB_TOKEN) return process.env.GITHUB_TOKEN;
  if (process.env.GH_TOKEN) return process.env.GH_TOKEN;

  try {
    return execFileSync("gh", ["auth", "token"], { encoding: "utf8" }).trim();
  } catch {
    throw new Error("set GITHUB_TOKEN or GH_TOKEN, or authenticate gh");
  }
}

function markdownEscape(value) {
  return value.replaceAll("|", "\\|").replaceAll("\n", " ");
}

function icon(name, label) {
  return `<img alt="${label}" src="${ICONS[name]}" width="16" height="16">`;
}

async function graphql(query, variables) {
  const response = await fetch("https://api.github.com/graphql", {
    method: "POST",
    headers: {
      Authorization: `Bearer ${githubToken()}`,
      "Content-Type": "application/json",
    },
    body: JSON.stringify({ query, variables }),
  });

  if (!response.ok) {
    throw new Error(`GitHub GraphQL returned ${response.status}`);
  }

  const body = await response.json();
  if (body.errors?.length) {
    throw new Error(body.errors.map((error) => error.message).join("; "));
  }

  return body.data;
}

async function publicRepos() {
  const repos = new Map();
  let after = null;

  for (;;) {
    const data = await graphql(
      `query($org: String!, $after: String) {
        organization(login: $org) {
          repositories(first: 100, after: $after, privacy: PUBLIC, orderBy: {field: NAME, direction: ASC}) {
            nodes {
              name
              description
              url
              isArchived
              isFork
              stargazerCount
              issues(states: OPEN) { totalCount }
              pullRequests(states: OPEN) { totalCount }
            }
            pageInfo {
              hasNextPage
              endCursor
            }
          }
        }
      }`,
      { org: ORG, after },
    );

    const page = data.organization.repositories;
    for (const repo of page.nodes) {
      if (!repo.isArchived && !repo.isFork) repos.set(repo.name, repo);
    }

    if (!page.pageInfo.hasNextPage) return repos;
    after = page.pageInfo.endCursor;
  }
}

function row(repo) {
  const description = repo.description?.replace(/\.$/, "") ?? "";

  return `| [${repo.name}](${repo.url}) | ${markdownEscape(description)} | ${repo.stargazerCount} | ${repo.issues.totalCount} | ${repo.pullRequests.totalCount} |`;
}

function table(group, repos) {
  const lines = [
    `### ${group.title}`,
    "",
    `| ${icon("repo", "Repository")} Repository | Description | ${icon("star", "Stars")} | ${icon("issue", "Open issues")} | ${icon("pr", "Open pull requests")} |`,
    "|------------|-------------|------:|-------:|----:|",
  ];

  for (const name of group.repos) {
    const repo = repos.get(name);
    if (!repo) throw new Error(`missing public repository: ${ORG}/${name}`);
    lines.push(row(repo));
  }

  return lines.join("\n");
}

function render(repos) {
  const tables = GROUPS.map((group) => table(group, repos)).join("\n\n");
  return `${START}\n<!-- This section is generated by scripts/update-profile-readme.mjs. -->\n\n${tables}\n${END}`;
}

function replaceGeneratedBlock(readme, block) {
  const start = readme.indexOf(START);
  const end = readme.indexOf(END);

  if (start === -1 || end === -1 || end < start) {
    throw new Error(`profile README must contain ${START} and ${END}`);
  }

  return `${readme.slice(0, start)}${block}${readme.slice(end + END.length)}`;
}

const repos = await publicRepos();
const readme = readFileSync(README, "utf8");
writeFileSync(README, replaceGeneratedBlock(readme, render(repos)));
